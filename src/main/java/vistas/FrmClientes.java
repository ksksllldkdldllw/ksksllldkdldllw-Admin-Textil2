    public void mostrarClientes() {

    try {

        conexion.Conexion objetoConexion = new conexion.Conexion();

        java.sql.Connection conexionDB = objetoConexion.establecerConexion();

        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();

        modelo.addColumn("Código");
        modelo.addColumn("Cedula");
        modelo.addColumn("Nombre");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Correo");
        modelo.addColumn("Dirección");

        jTable1.setModel(modelo);

        String sql = "SELECT * FROM clientes";

        java.sql.Statement st = conexionDB.createStatement();

        java.sql.ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {

            

            String[] datos = new String[6];

datos[0] = rs.getString("Codigo");
datos[1] = rs.getString("Cedula");
datos[2] = rs.getString("Nombre");
datos[3] = rs.getString("Telefono");
datos[4] = rs.getString("Correo");
datos[5] = rs.getString("Direccion");

            modelo.addRow(datos);
        }

    } catch (Exception e) {

        javax.swing.JOptionPane.showMessageDialog(null, "Error al mostrar clientes: " + e.toString());

    }
}
