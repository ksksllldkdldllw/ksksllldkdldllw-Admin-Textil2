    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
if (jTextField1.getText().trim().isEmpty()
            && txtCedula.getText().trim().isEmpty()
            && jTextField2.getText().trim().isEmpty()
            && jTextField3.getText().trim().isEmpty()
            && jTextField4.getText().trim().isEmpty()
            && jTextField5.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(
                null,
                "Debe ingresar al menos un cliente para guardar");

        return;
    }

        try {

    conexion.Conexion objetoConexion = new conexion.Conexion();

    java.sql.Connection conexionDB = objetoConexion.establecerConexion();

String sql = "INSERT INTO clientes "
           + "(Codigo, Cedula, Nombre, Telefono, Correo, Direccion) "
           + "VALUES (?, ?, ?, ?, ?, ?)";
    java.sql.PreparedStatement pst = conexionDB.prepareStatement(sql);

pst.setInt(1, Integer.parseInt(jTextField1.getText()));
pst.setString(2, txtCedula.getText());
pst.setString(3, jTextField2.getText());
pst.setString(4, jTextField3.getText());
pst.setString(5, jTextField4.getText());
pst.setString(6, jTextField5.getText());

pst.executeUpdate();

    javax.swing.JOptionPane.showMessageDialog(null, "Cliente guardado");
 mostrarClientes();
 limpiar();
} catch (Exception e) {

    javax.swing.JOptionPane.showMessageDialog(null, "Error: " + e.toString());

}        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

